package Day9;

public class PrimitiveCharExample {
    public static void main(String[] args) {
        char[] charArray = {'A', 'B', 'H', 'I','N','A','Y'};
        System.out.println("Character Array Elements:");
        for (char c : charArray) {
            System.out.print(c + " ");
        }
        System.out.println(); // New line for proper formatting

    }
}
