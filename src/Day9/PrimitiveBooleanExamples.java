package Day9;

public class PrimitiveBooleanExamples {
    // Array of booleans (boolean)
    public static void main(String[] args) {
        boolean[] PrimitiveBooleanExamples = new boolean[3]; // Declared with a size of 3
        PrimitiveBooleanExamples[0] = true;
        PrimitiveBooleanExamples[1] = false;
        PrimitiveBooleanExamples[2] = true;
        System.out.println("Boolean Array Elements:");
        for(boolean ba : PrimitiveBooleanExamples) { // Enhanced for loop

            System.out.println(ba+ "");
        }


        System.out.println(); // New line for separation
    }
    }

